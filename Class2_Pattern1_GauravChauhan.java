package class2Assignment;

//basic pattern printing program

public class Class2_Pattern1_GauravChauhan {
          public static void main(String[] args) {
          int i = 5, j = 1;
          while (i >= 1) {
             j = 1;
          while (j <= i) {
            System.out.print("1");
            j++;
            }
            System.out.println("");
            i--;
        }

    }
}