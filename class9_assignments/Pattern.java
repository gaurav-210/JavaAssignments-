public class Pattern {

	public static void main(String args[])
    {
        int i=0;
        int j=0;
        char ch='a';
        
        for(i=0;i<3;i++)
        {ch='a';
            for(j=0;j<=i;j++)
            {
                System.out.print(""+ch);
                ch++;
                
            }
            System.out.println("");
        }
    }
	
}













